import torch
import torch.nn as nn
from torch.nn import init
import functools

class Pix2pixNet(nn.Module):
    def __init__(self, input_nc, output_nc, ngf=64, netG='unet_256'):
        super().__init__()
        self.netG = define_G(input_nc, output_nc, ngf, netG)

    def forward(self, x, c):
        return self.netG(x, c)

